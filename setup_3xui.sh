#!/bin/bash

# –°–∫—Ä–∏–ø—Ç –¥–ª—è –∞–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–æ–π —É—Å—Ç–∞–Ω–æ–≤–∫–∏ 3x-ui –ø–∞–Ω–µ–ª–∏ –Ω–∞ Ubuntu/Debian —Å–µ—Ä–≤–µ—Ä
# –ê–≤—Ç–æ—Ä: AI Assistant
# –î–∞—Ç–∞: 03.02.2026
# –ó–∞–ø—É—Å–∫–∞—Ç—å —Å –ø—Ä–∞–≤–∞–º–∏ root –∏–ª–∏ —á–µ—Ä–µ–∑ sudo

# –í–∫–ª—é—á–µ–Ω–∏–µ —Å—Ç—Ä–æ–≥–æ–≥–æ —Ä–µ–∂–∏–º–∞: –≤—ã—Ö–æ–¥ –ø—Ä–∏ –æ—à–∏–±–∫–µ, –≤—ã–≤–æ–¥ –∫–æ–º–∞–Ω–¥
set -e
set -x

# –§—É–Ω–∫—Ü–∏—è –¥–ª—è –æ–±—Ä–∞–±–æ—Ç–∫–∏ –æ—à–∏–±–æ–∫
error_exit() {
    echo "–û—à–∏–±–∫–∞: $1"
    exit 1
}

# –ü—Ä–æ–≤–µ—Ä–∫–∞, –∑–∞–ø—É—â–µ–Ω –ª–∏ —Å–∫—Ä–∏–ø—Ç —Å sudo
if [ "$EUID" -ne 0 ]; then
    error_exit "–ó–∞–ø—É—Å—Ç–∏—Ç–µ —Å–∫—Ä–∏–ø—Ç —Å –ø—Ä–∞–≤–∞–º–∏ root (sudo ./setup_3xui.sh)"
fi

echo "=== –ù–∞—á–∞–ª–æ —É—Å—Ç–∞–Ω–æ–≤–∫–∏ 3x-ui ==="

# –®–∞–≥ 1: –û–±–Ω–æ–≤–ª–µ–Ω–∏–µ –ø–∞–∫–µ—Ç–æ–≤
echo "–®–∞–≥ 1: –û–±–Ω–æ–≤–ª–µ–Ω–∏–µ —Å–ø–∏—Å–∫–∞ –ø–∞–∫–µ—Ç–æ–≤..."
apt update || error_exit "–ù–µ —É–¥–∞–ª–æ—Å—å –æ–±–Ω–æ–≤–∏—Ç—å –ø–∞–∫–µ—Ç—ã"

# –®–∞–≥ 2: –£—Å—Ç–∞–Ω–æ–≤–∫–∞ Git
echo "–®–∞–≥ 2: –£—Å—Ç–∞–Ω–æ–≤–∫–∞ Git..."
apt install -y git || error_exit "–ù–µ —É–¥–∞–ª–æ—Å—å —É—Å—Ç–∞–Ω–æ–≤–∏—Ç—å Git"

# –®–∞–≥ 3: –£—Å—Ç–∞–Ω–æ–≤–∫–∞ Docker –∏ Docker Compose
echo "–®–∞–≥ 3: –£—Å—Ç–∞–Ω–æ–≤–∫–∞ Docker –∏ Docker Compose..."
apt install -y docker.io docker-compose || error_exit "–ù–µ —É–¥–∞–ª–æ—Å—å —É—Å—Ç–∞–Ω–æ–≤–∏—Ç—å Docker"

# –®–∞–≥ 4: –ö–ª–æ–Ω–∏—Ä–æ–≤–∞–Ω–∏–µ —Ä–µ–ø–æ–∑–∏—Ç–æ—Ä–∏—è 3x-ui
echo "–®–∞–≥ 4: –ö–ª–æ–Ω–∏—Ä–æ–≤–∞–Ω–∏–µ —Ä–µ–ø–æ–∑–∏—Ç–æ—Ä–∏—è 3x-ui..."
git clone https://github.com/MHSanaei/3x-ui.git || error_exit "–ù–µ —É–¥–∞–ª–æ—Å—å –∫–ª–æ–Ω–∏—Ä–æ–≤–∞—Ç—å —Ä–µ–ø–æ–∑–∏—Ç–æ—Ä–∏–π"

# –®–∞–≥ 5: –ü–µ—Ä–µ—Ö–æ–¥ –≤ –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏—é
echo "–®–∞–≥ 5: –ü–µ—Ä–µ—Ö–æ–¥ –≤ –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏—é 3x-ui..."
cd 3x-ui || error_exit "–î–∏—Ä–µ–∫—Ç–æ—Ä–∏—è 3x-ui –Ω–µ –Ω–∞–π–¥–µ–Ω–∞"

# –®–∞–≥ 6: –ü–µ—Ä–µ–∫–ª—é—á–µ–Ω–∏–µ –Ω–∞ –≤–µ—Ä—Å–∏—é
echo "–®–∞–≥ 6: –ü–µ—Ä–µ–∫–ª—é—á–µ–Ω–∏–µ –Ω–∞ –≤–µ—Ä—Å–∏—é v1.4.6..."
git checkout v1.4.6 || error_exit "–ù–µ —É–¥–∞–ª–æ—Å—å –ø–µ—Ä–µ–∫–ª—é—á–∏—Ç—å—Å—è –Ω–∞ –≤–µ—Ä—Å–∏—é v1.4.6"

# –®–∞–≥ 7: –ó–∞–ø—É—Å–∫ –≤ —Ñ–æ–Ω–µ
echo "–®–∞–≥ 7: –ó–∞–ø—É—Å–∫ 3x-ui –≤ —Ñ–æ–Ω–µ..."
docker-compose up -d || error_exit "–ù–µ —É–¥–∞–ª–æ—Å—å –∑–∞–ø—É—Å—Ç–∏—Ç—å 3x-ui"

# –®–∞–≥ 8: –û–ø—Ä–µ–¥–µ–ª–µ–Ω–∏–µ IP-–∞–¥—Ä–µ—Å–∞ –∏ –ø—Ä–æ–≤–µ—Ä–∫–∞ –¥–æ—Å—Ç—É–ø–Ω–æ—Å—Ç–∏
echo "–®–∞–≥ 8: –û–ø—Ä–µ–¥–µ–ª–µ–Ω–∏–µ –≤–Ω–µ—à–Ω–µ–≥–æ IP-–∞–¥—Ä–µ—Å–∞ —Å–µ—Ä–≤–µ—Ä–∞..."
SERVER_IP=$(curl -s --connect-timeout 5 ifconfig.me 2>/dev/null || \
            curl -s --connect-timeout 5 ipinfo.io/ip 2>/dev/null || \
            curl -s --connect-timeout 5 icanhazip.com 2>/dev/null || \
            hostname -I 2>/dev/null | awk '{print $1}')

if [ -z "$SERVER_IP" ] || [ "$SERVER_IP" = "127.0.0.1" ] || [ "$SERVER_IP" = "localhost" ]; then
    echo "‚ö†Ô∏è  –ù–µ —É–¥–∞–ª–æ—Å—å –æ–ø—Ä–µ–¥–µ–ª–∏—Ç—å –≤–Ω–µ—à–Ω–∏–π IP. –í–æ–∑–º–æ–∂–Ω–æ, —Å–µ—Ä–≤–µ—Ä –∑–∞ NAT –∏–ª–∏ –±–µ–∑ –∏–Ω—Ç–µ—Ä–Ω–µ—Ç–∞."
    echo "   –ü–æ–ø—Ä–æ–±—É–π—Ç–µ –∏—Å–ø–æ–ª—å–∑–æ–≤–∞—Ç—å –ª–æ–∫–∞–ª—å–Ω—ã–π IP: $(hostname -I | awk '{print $1}')"
    SERVER_IP="yourserverip"
fi

PANEL_URL="http://$SERVER_IP:2053/panel/"

# –ü—Ä–æ–≤–µ—Ä–∫–∞ –¥–æ—Å—Ç—É–ø–Ω–æ—Å—Ç–∏ –ø–∞–Ω–µ–ª–∏ —Å –ø–æ–≤—Ç–æ—Ä–Ω—ã–º–∏ –ø–æ–ø—ã—Ç–∫–∞–º–∏
echo "–ü—Ä–æ–≤–µ—Ä–∫–∞ –¥–æ—Å—Ç—É–ø–Ω–æ—Å—Ç–∏ –ø–∞–Ω–µ–ª–∏ 3x-ui..."
MAX_ATTEMPTS=5
ATTEMPT=1

while [ $ATTEMPT -le $MAX_ATTEMPTS ]; do
    echo "–ü–æ–ø—ã—Ç–∫–∞ $ATTEMPT/$MAX_ATTEMPTS..."
    if curl -s --max-time 10 --connect-timeout 5 "$PANEL_URL" > /dev/null 2>&1; then
        echo "‚úÖ –ü–∞–Ω–µ–ª—å 3x-ui –¥–æ—Å—Ç—É–ø–Ω–∞ –ø–æ –∞–¥—Ä–µ—Å—É: $PANEL_URL"
        break
    else
        if [ $ATTEMPT -eq $MAX_ATTEMPTS ]; then
            echo "‚ùå –ü–∞–Ω–µ–ª—å –Ω–µ –¥–æ—Å—Ç—É–ø–Ω–∞ –ø–æ—Å–ª–µ $MAX_ATTEMPTS –ø–æ–ø—ã—Ç–æ–∫."
            echo "   –ü—Ä–æ–≤–µ—Ä—å—Ç–µ:"
            echo "   - –ü—Ä–∞–≤–∏–ª—å–Ω–æ—Å—Ç—å IP: $SERVER_IP"
            echo "   - –û—Ç–∫—Ä—ã—Ç –ª–∏ –ø–æ—Ä—Ç 2053 –≤ firewall"
            echo "   - –ó–∞–ø—É—â–µ–Ω –ª–∏ Docker –∫–æ–Ω—Ç–µ–π–Ω–µ—Ä: docker ps"
        else
            echo "‚è≥ –ü–∞–Ω–µ–ª—å –µ—â–µ –Ω–µ –≥–æ—Ç–æ–≤–∞, –∂–¥–µ–º 15 —Å–µ–∫—É–Ω–¥..."
            sleep 15
        fi
    fi
    ((ATTEMPT++))
done

# –®–∞–≥ 9: –ó–∞–≤–µ—Ä—à–µ–Ω–∏–µ
echo ""
echo "=== –£—Å—Ç–∞–Ω–æ–≤–∫–∞ –∑–∞–≤–µ—Ä—à–µ–Ω–∞ —É—Å–ø–µ—à–Ω–æ! ==="
echo ""
echo "üåê –î–ª—è –¥–æ—Å—Ç—É–ø–∞ –∫ 3X-UI –æ—Ç–∫—Ä–æ–π—Ç–µ –±—Ä–∞—É–∑–µ—Ä –∏ –ø–µ—Ä–µ–π–¥–∏—Ç–µ –ø–æ –∞–¥—Ä–µ—Å—É:"
echo "   $PANEL_URL"
echo ""
echo "üîë –î–∞–Ω–Ω—ã–µ –¥–ª—è –≤—Ö–æ–¥–∞:"
echo "   –õ–æ–≥–∏–Ω: admin"
echo "   –ü–∞—Ä–æ–ª—å: admin"
echo ""
echo "‚ö†Ô∏è  –í–∞–∂–Ω–æ: –°—Ä–∞–∑—É –∏–∑–º–µ–Ω–∏—Ç–µ –ø–∞—Ä–æ–ª—å –ø–æ—Å–ª–µ –ø–µ—Ä–≤–æ–≥–æ –≤—Ö–æ–¥–∞!"
echo ""
echo "üí° –ï—Å–ª–∏ IP –Ω–µ –ø—Ä–∞–≤–∏–ª—å–Ω—ã–π, –∑–∞–º–µ–Ω–∏—Ç–µ $SERVER_IP –Ω–∞ –≤–∞—à —Ä–µ–∞–ª—å–Ω—ã–π IP-–∞–¥—Ä–µ—Å —Å–µ—Ä–≤–µ—Ä–∞."
